@app.callback(
    Output("indexed-chart",    "figure"),
    Output("equity-bar-chart", "figure"),
    Output("dual-axis-chart",  "figure"),
    Output("snapshot-table",   "figure"),
    Input("date-picker",       "start_date"),
    Input("date-picker",       "end_date"),
    Input("series-dropdown",   "value"),
)
def update_all(start_date, end_date, selected_series):
    start = pd.Timestamp(start_date) if start_date else DATE_MIN
    end   = pd.Timestamp(end_date)   if end_date   else DATE_MAX

    # ── CHART 1: Indexed performance line chart ──────────────────────────────
    fig_indexed = go.Figure()

    if not indexed_df.empty and selected_series:
        mask     = (indexed_df.index >= start) & (indexed_df.index <= end)
        filtered = indexed_df.loc[mask]

        for col in (selected_series or []):
            if col not in filtered.columns:
                continue
            s    = filtered[col].dropna()
            meta = SERIES_META.get(col, {"label": col, "color": BLUE})
            fig_indexed.add_trace(go.Scatter(
                x=s.index, y=s.values,
                name=meta["label"],
                mode="lines",
                line=dict(color=meta["color"], width=2),
                hovertemplate=f"<b>{meta['label']}</b><br>%{{x|%b %d, %Y}}: %{{y:.1f}}<extra></extra>",
            ))

    # D0 reference lines
    fig_indexed.add_hline(y=100, line_dash="dot", line_color="#3a3a3a", line_width=1)
    if start <= pd.Timestamp(CONFLICT_START) <= end:
        fig_indexed.add_vline(x=CONFLICT_START, line_dash="dash", line_color=RED, line_width=1)
        fig_indexed.add_annotation(
            x=CONFLICT_START, y=1, yref="paper",
            text="D0", font=dict(color=RED, size=10),
            showarrow=False, xanchor="left", xshift=4,
        )

    fig_indexed.update_layout(
        **CHART_BASE, height=370,
        yaxis_title="Index (D0 = 100)",
        hovermode="x unified",
        xaxis=dict(gridcolor=GRID, linecolor=BORDER, tickcolor=BORDER),
        yaxis=dict(gridcolor=GRID, linecolor=BORDER, tickcolor=BORDER),
    )

    # ── CHART 2: Equity returns horizontal bar ───────────────────────────────
    EQUITY_SERIES = [
        ("sp500",       "S&P 500",       equity_df),
        ("nasdaq",      "Nasdaq",        equity_df),
        ("eurostoxx50", "Euro Stoxx 50", equity_df),
        ("dax",         "DAX",           equity_df),
        ("ftse100",     "FTSE 100",      equity_df),
        ("nikkei225",   "Nikkei 225",    equity_df),
        ("shanghai",    "Shanghai",      equity_df),
    ]

    labels, values, bar_colors = [], [], []
    for col, label, src in EQUITY_SERIES:
        delta_col = f"{col}_delta_vs_d0_pct"
        if src.empty or delta_col not in src.columns:
            continue
        s = src.loc[src.index <= end, delta_col].dropna()
        if s.empty:
            continue
        v = float(s.iloc[-1])
        labels.append(label)
        values.append(v)
        bar_colors.append(RED if v < 0 else BLUE)

    # Sort ascending so largest positive is at top
    if values:
        pairs = sorted(zip(values, labels, bar_colors))
        values, labels, bar_colors = map(list, zip(*pairs))

    fig_equity = go.Figure(go.Bar(
        x=values, y=labels,
        orientation="h",
        marker_color=bar_colors,
        text=[f"{v:+.1f}%" for v in values],
        textposition="outside",
        textfont=dict(size=11, color=TEXT),
        hovertemplate="<b>%{y}</b><br>%{x:+.2f}%<extra></extra>",
    ))
    fig_equity.add_vline(x=0, line_color="#3a3a3a", line_width=1)
    fig_equity.update_layout(
        **CHART_BASE, height=370,
        showlegend=False,
        xaxis=dict(
            gridcolor=GRID, linecolor=BORDER, tickcolor=BORDER,
            ticksuffix="%", title="% change since D0",
        ),
        yaxis=dict(gridcolor=GRID, linecolor=BORDER, tickcolor=BORDER),
    )

    # ── CHART 3: Dual-axis Brent + US 10Y (with range slider) ───────────────
    fig_dual = make_subplots(specs=[[{"secondary_y": True}]])

    if not energy_df.empty and "brent_usd" in energy_df.columns:
        bm = (energy_df.index >= start) & (energy_df.index <= end)
        b  = energy_df.loc[bm, "brent_usd"].dropna()
        fig_dual.add_trace(
            go.Scatter(
                x=b.index, y=b.values, name="Brent ($/bbl)",
                line=dict(color=RED, width=2),
                hovertemplate="<b>Brent</b>: $%{y:.2f}<extra></extra>",
            ),
            secondary_y=False,
        )

    if not rates_df.empty and "us10y_yield" in rates_df.columns:
        rm = (rates_df.index >= start) & (rates_df.index <= end)
        r  = rates_df.loc[rm, "us10y_yield"].dropna()
        if not r.empty:
            fig_dual.add_trace(
                go.Scatter(
                    x=r.index, y=r.values, name="US 10Y (%)",
                    line=dict(color=BLUE, width=2, dash="dot"),
                    hovertemplate="<b>US 10Y</b>: %{y:.2f}%<extra></extra>",
                ),
                secondary_y=True,
            )

    fig_dual.update_layout(
        **CHART_BASE,
        height=370,
        hovermode="x unified",
    )
    fig_dual.update_xaxes(
        gridcolor=GRID, linecolor=BORDER, tickcolor=BORDER,
        rangeslider=dict(visible=True, bgcolor=BG_CARD, thickness=0.06),
    )
    fig_dual.update_yaxes(
        title_text="Brent ($/bbl)",
        gridcolor=GRID, linecolor=BORDER, tickcolor=BORDER,
        secondary_y=False,
    )
    fig_dual.update_yaxes(
        title_text="US 10Y Yield (%)",
        gridcolor=GRID, linecolor=BORDER, tickcolor=BORDER,
        showgrid=False,
        secondary_y=True,
    )

    # ── CHART 4: Snapshot table ──────────────────────────────────────────────
    t_labels, t_current, t_d7, t_d0 = [], [], [], []
    fc_current, fc_d7, fc_d0        = [], [], []

    for var in TABLE_VARS:
        col = var["col"]
        src = var["src"]
        bad_up = var["bad_up"]
        fmt    = var["fmt"]

        if src.empty or col not in src.columns:
            # Gracefully skip missing series (e.g. FRED data not loaded)
            continue

        s = src.loc[src.index <= end, col].dropna()
        if s.empty:
            continue

        cur_val = float(s.iloc[-1])
        t_labels.append(var["label"])
        t_current.append(f"{cur_val:{fmt}}")
        fc_current.append(TEXT)

        # Δ 7d — compare to latest value ≤ (last_date - 7 calendar days)
        cutoff = s.index[-1] - pd.Timedelta(days=7)
        s_prior = s[s.index <= cutoff]
        if not s_prior.empty:
            v7  = float(s_prior.iloc[-1])
            d7  = delta_pct(cur_val, v7)
            t_d7.append(fmt_delta(d7))
            fc_d7.append(risk_color(d7, bad_up))
        else:
            t_d7.append("N/A")
            fc_d7.append(TEXT_DIM)

        # Δ since D0 — from precomputed column
        d0_col = f"{col}_delta_vs_d0_pct"
        d0_val = safe_last(src, d0_col, as_of=end)
        t_d0.append(fmt_delta(d0_val))
        fc_d0.append(risk_color(d0_val, bad_up))

    # Row count for table height
    n_rows = max(len(t_labels), 1)

    fig_table = go.Figure(go.Table(
        columnwidth=[2.2, 1.1, 1.0, 1.2],
        header=dict(
            values=["<b>Variable</b>", "<b>Current</b>", "<b>Δ 7d</b>", "<b>Δ since D0</b>"],
            fill_color="#1e1e1e",
            font=dict(color=TEXT, size=12, family="system-ui"),
            align=["left", "right", "right", "right"],
            line_color=BORDER,
            height=28,
        ),
        cells=dict(
            values=[t_labels, t_current, t_d7, t_d0],
            fill_color=BG_CARD,
            # Per-cell conditional colors: list-of-lists, one per column
            font=dict(
                color=[fc_current, fc_current, fc_d7, fc_d0],
                size=11,
                family="system-ui",
            ),
            align=["left", "right", "right", "right"],
            line_color=BORDER,
            height=23,
        ),
    ))
    fig_table.update_layout(
        paper_bgcolor=BG_CHART,
        margin=dict(l=0, r=0, t=0, b=0),
        height=max(370, 28 + n_rows * 23 + 10),
    )

    return fig_indexed, fig_equity, fig_dual, fig_table


