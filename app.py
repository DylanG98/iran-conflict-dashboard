def safe_last(df, col, as_of=None):
    """Return the last non-null value of df[col], optionally up to as_of date."""
    if df.empty or col not in df.columns:
        return None
    s = df[col].dropna()
    if as_of is not None:
        s = s[s.index <= pd.Timestamp(as_of)]
    return float(s.iloc[-1]) if not s.empty else None


def safe_d0_val(df, col):
    """Return the first value of df[col] on or after CONFLICT_START."""
    if df.empty or col not in df.columns:
        return None
    post = df.loc[df.index >= pd.Timestamp(CONFLICT_START), col].dropna()
    return float(post.iloc[0]) if not post.empty else None


def delta_pct(current, d0):
    if current is None or d0 is None or d0 == 0:
        return None
    return (current - d0) / abs(d0) * 100


def fmt_delta(v, is_bps=False):
    if v is None:
        return "N/A"
    return f"{v:+.0f} bps" if is_bps else f"{v:+.1f}%"


def risk_color(v, higher_is_bad=True):
    """Red if adverse, blue if beneficial. Returns TEXT_DIM for None."""
    if v is None:
        return TEXT_DIM
    return RED if (v > 0) == higher_is_bad else BLUE


# ── Compute KPI cards at load time ──────────────────────────────────────────
def _compute_kpis():
    kpis = {}

    # Brent — higher oil = bad (conflict risk)
    v = safe_last(energy_df, "brent_usd")
    d = safe_last(energy_df, "brent_usd_delta_vs_d0_pct")
    kpis["brent"] = (f"{v:.2f}" if v else "N/A", fmt_delta(d), risk_color(d, True))

    # Gold — higher gold = safe-haven (blue = good signal)
    v = safe_last(energy_df, "gold_usd")
    d = safe_last(energy_df, "gold_usd_delta_vs_d0_pct")
    kpis["gold"] = (f"{v:,.0f}" if v else "N/A", fmt_delta(d), risk_color(d, False))

    # VIX — higher = more fear = bad
    v = safe_last(vol_df, "vix")
    d = safe_last(vol_df, "vix_delta_vs_d0_pct")
    kpis["vix"] = (f"{v:.1f}" if v else "N/A", fmt_delta(d), risk_color(d, True))

    # DXY — higher = USD strengthening = risk-off signal
    v = safe_last(vol_df, "dxy")
    d = safe_last(vol_df, "dxy_delta_vs_d0_pct")
    kpis["dxy"] = (f"{v:.1f}" if v else "N/A", fmt_delta(d), risk_color(d, True))

    # US 10Y — delta in basis points
    v   = safe_last(rates_df, "us10y_yield")
    d0  = safe_d0_val(rates_df, "us10y_yield")
    bps = (v - d0) * 100 if v and d0 else None
    kpis["us10y"] = (f"{v:.2f}%" if v else "N/A", fmt_delta(bps, True), risk_color(bps, True))

    # 2Y-10Y spread — wider spread can indicate growth expectations
    if (not rates_df.empty
            and "us10y_yield" in rates_df.columns
            and "us2y_yield" in rates_df.columns):
        merged = rates_df[["us10y_yield", "us2y_yield"]].dropna()
        if not merged.empty:
            spread = merged["us10y_yield"] - merged["us2y_yield"]
            v_bps  = float(spread.iloc[-1]) * 100
            post   = spread[spread.index >= pd.Timestamp(CONFLICT_START)]
            d0_bps = float(post.iloc[0]) * 100 if not post.empty else None
            delta  = (v_bps - d0_bps) if d0_bps is not None else None
            kpis["spread"] = (f"{v_bps:.0f} bps", fmt_delta(delta, True), risk_color(delta, False))
        else:
            kpis["spread"] = ("N/A", "N/A", TEXT_DIM)
    else:
        kpis["spread"] = ("N/A", "N/A", TEXT_DIM)

    return kpis


KPIS = _compute_kpis()

